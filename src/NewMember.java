    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id= jLabel3.getText();
        String name=jTextField1.getText();
        String mobile=jTextField2.getText();
        String nic=jTextField3.getText();
        String gender=(String)jComboBox1.getSelectedItem();
        String email=jTextField4.getText();
        String address=jTextArea1.getText();
        String age=jTextField7.getText();
        String gymtime=(String)jComboBox2.getSelectedItem();
        String amount=jTextField8.getText();
        String gname=jTextField5.getText();
        String gmobile=jTextField6.getText();
        
        try {
            Connection con=ConnectionProvider.getCon();
            PreparedStatement ps= con.prepareStatement("insert into member values (?,?,?,?,?,?,?,?,?,?,?,?)");
            ps.setString(1,id);
            ps.setString(2,name);
            ps.setString(3,mobile);
            ps.setString(4,nic);
            ps.setString(5,gender);
            ps.setString(6,email);
            ps.setString(7,address);
            ps.setString(8,age);
            ps.setString(9,gymtime);
            ps.setString(10,amount);
            ps.setString(11,gname);
            ps.setString(12,gmobile);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "Successfuly Saved.");
            
            setVisible(false);
            new NewMember().setVisible(true);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
       
    }//GEN-LAST:event_jButton2ActionPerformed

